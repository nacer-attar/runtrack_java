public class Addition {
    public static void main (String[] args) {
        int a = 3;
        int b = 73;
        /* double pour afficher des décimales
        (double) pour forcer la conversion de a et b avant la division
         pour que le résultat ne soit pas un entier
         */
        double res = (double) a/b;
        System.out.println(res);
    }
}
